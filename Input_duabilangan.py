Bilangan_pertama = float(input("Masukan bilangan pertama: "))
Bilangan_kedua = float(input("Masukan bilangan kedua: "))

if Bilangan_pertama > Bilangan_kedua:
    print(Bilangan_pertama)
elif Bilangan_kedua > Bilangan_pertama:
    print(Bilangan_kedua)  
else:
    print("Kedua bilangan sama besar:", Bilangan_pertama)      
    print("Bilangan yang paling besar adalah:", Bilangan_pertama)